def interpolate_yield_strength(temp):
    """
    –õ–∏–Ω–µ–π–Ω–∞—è –∏–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏—è –Ω–æ—Ä–º–∞—Ç–∏–≤–Ω–æ–≥–æ –ø—Ä–µ–¥–µ–ª–∞ —Ç–µ–∫—É—á–µ—Å—Ç–∏ –¥–ª—è —Å—Ç–∞–ª–∏ –º–∞—Ä–∫–∏ 20.
    –ò–∑–≤–µ—Å—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è:
    - 20¬∞C: 216 –ú–ü–∞ (–∏–∑ —à–∞–±–ª–æ–Ω–∞)
    - 250¬∞C: 196 –ú–ü–∞
    - 400¬∞C: 137 –ú–ü–∞
    - 450¬∞C: 127 –ú–ü–∞
    """
    # –ò–∑–≤–µ—Å—Ç–Ω—ã–µ —Ç–æ—á–∫–∏
    known_points = [
        (20, 216),
        (250, 196),
        (400, 137),
        (450, 127)
    ]
    
    # –ï—Å–ª–∏ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –∏–∑–≤–µ—Å—Ç–Ω–æ–π —Ç–æ—á–∫–æ–π
    for t, value in known_points:
        if temp == t:
            return value
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∏–Ω—Ç–µ—Ä–≤–∞–ª –¥–ª—è –∏–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏–∏
    if temp < 20:
        return 216  # –î–ª—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä –Ω–∏–∂–µ 20¬∞C –∏—Å–ø–æ–ª—å–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è 20¬∞C
    elif 20 < temp <= 250:
        # –ò–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏—è –º–µ–∂–¥—É 20¬∞C –∏ 250¬∞C
        x1, y1 = 20, 216
        x2, y2 = 250, 196
    elif 250 < temp <= 400:
        # –ò–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏—è –º–µ–∂–¥—É 250¬∞C –∏ 400¬∞C
        x1, y1 = 250, 196
        x2, y2 = 400, 137
    elif 400 < temp <= 450:
        # –ò–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏—è –º–µ–∂–¥—É 400¬∞C –∏ 450¬∞C
        x1, y1 = 400, 137
        x2, y2 = 450, 127
    else:
        # –î–ª—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä –≤—ã—à–µ 450¬∞C –∏—Å–ø–æ–ª—å–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è 450¬∞C
        return 127
    
    # –õ–∏–Ω–µ–π–Ω–∞—è –∏–Ω—Ç–µ—Ä–ø–æ–ª—è—Ü–∏—è: y = y1 + (y2 - y1) * (x - x1) / (x2 - x1)
    result = y1 + (y2 - y1) * (temp - x1) / (x2 - x1)
    
    # –û–∫—Ä—É–≥–ª–µ–Ω–∏–µ –¥–æ —Ü–µ–ª–æ–≥–æ
    return round(result)

def parse_protocol_from_docx(file_content):
    """–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞–Ω–Ω—ã—Ö –∏–∑ DOCX —Ñ–∞–π–ª–∞ —Å –ø—Ä–æ—Ç–æ–∫–æ–ª–æ–º"""
    doc = Document(BytesIO(file_content))
    
    # –ò—â–µ–º —Ç–∞–±–ª–∏—Ü—ã –≤ –¥–æ–∫—É–º–µ–Ω—Ç–µ
    data_rows = []
    
    for table in doc.tables:
        # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º —Å—Ç—Ä–æ–∫–∞–º —Ç–∞–±–ª–∏—Ü—ã
        for i, row in enumerate(table.rows):
            cells = [cell.text.strip() for cell in row.cells]
            
            # –ò—â–µ–º —Å—Ç—Ä–æ–∫–∏ —Å –∫–ª–µ–π–º–æ–º –æ–±—Ä–∞–∑—Ü–∞ (—Ñ–æ—Ä–º–∞—Ç X-Y)
            for cell_text in cells:
                if re.match(r'^\d+-\d+$', cell_text):
                    try:
                        sample_mark = cell_text
                        
                        # –ù–∞ –æ—Å–Ω–æ–≤–µ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã –≤–∞—à–µ–π —Ç–∞–±–ª–∏—Ü—ã –æ–ø—Ä–µ–¥–µ–ª—è–µ–º –∏–Ω–¥–µ–∫—Å—ã
                        # –í –≤–∞—à–µ–π —Ç–∞–±–ª–∏—Ü–µ –¥–∞–Ω–Ω—ã–µ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã—Ö –∫–æ–ª–æ–Ω–∫–∞—Ö
                        # –ü–æ –æ–±—Ä–∞–∑—Ü—É –∏–∑ —Ñ–∞–π–ª–∞:
                        # | –ö–ª–µ–π–º–æ | –î–ª–∏–Ω–∞ | –î–∏–∞–º–µ—Ç—Ä | –ü–ª–æ—â–∞–¥—å | –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ | –°–∫–æ—Ä–æ—Å—Ç—å | –î–∏–∞–º–µ—Ç—Ä –ø–æ—Å–ª–µ | –ü–ª–æ—â–∞–¥—å –ø–æ—Å–ª–µ | –ú–∞–∫—Å.–Ω–∞–≥—Ä—É–∑–∫–∞ | –ü—Ä–µ–¥–µ–ª –ø—Ä–æ—á–Ω–æ—Å—Ç–∏ | –ü—Ä–µ–¥–µ–ª —Ç–µ–∫—É—á–µ—Å—Ç–∏ | –û—Ç–Ω.—Å—É–∂–µ–Ω–∏–µ | –û—Ç–Ω.—É–¥–ª–∏–Ω–µ–Ω–∏–µ |
                        # | 1-1    | 15    | 2,97    | 6,93    | 20          | 1        | 1,94          | 2,96          | 3 363         | 485              | 297              | 57          | 30           |
                        
                        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤—Å–µ —è—á–µ–π–∫–∏ –≤ —Å—Ç—Ä–æ–∫—É –¥–ª—è –ø–æ–∏—Å–∫–∞
                        row_text = ' '.join(cells)
                        
                        # –ò—â–µ–º –≤—Å–µ —á–∏—Å–ª–∞ –≤ —Å—Ç—Ä–æ–∫–µ
                        numbers = re.findall(r'[\d]+[.,]?\d*', row_text)
                        
                        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫–∏ –≤ —á–∏—Å–ª–∞ (–∑–∞–º–µ–Ω—è–µ–º –∑–∞–ø—è—Ç—ã–µ –Ω–∞ —Ç–æ—á–∫–∏)
                        numbers = [float(num.replace(',', '.').replace(' ', '')) for num in numbers if num]
                        
                        if len(numbers) >= 13:  # –î–æ–ª–∂–Ω–æ –±—ã—Ç—å –º–∏–Ω–∏–º—É–º 13 —á–∏—Å–µ–ª
                            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∏–Ω–¥–µ–∫—Å—ã –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã
                            # –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ –æ–±—ã—á–Ω–æ –ø–æ—Å–ª–µ –∫–ª–µ–π–º–∞ –∏ —Ä–∞–∑–º–µ—Ä–æ–≤
                            # –í –≤–∞—à–µ–º —Ñ–æ—Ä–º–∞—Ç–µ: –ø–æ—Å–ª–µ –∫–ª–µ–π–º–∞ –∏–¥—É—Ç: –¥–ª–∏–Ω–∞(15), –¥–∏–∞–º–µ—Ç—Ä(2.97), –ø–ª–æ—â–∞–¥—å(6.93), —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞(20)
                            # –ó–Ω–∞—á–∏—Ç —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞ - 4-–µ —á–∏—Å–ª–æ (–∏–Ω–¥–µ–∫—Å 3)
                            temperature = int(numbers[3]) if len(numbers) > 3 else 20
                            
                            # –ü—Ä–µ–¥–µ–ª –ø—Ä–æ—á–Ω–æ—Å—Ç–∏ –∏ —Ç–µ–∫—É—á–µ—Å—Ç–∏ –Ω–∞—Ö–æ–¥—è—Ç—Å—è –¥–∞–ª—å—à–µ
                            # –í –≤–∞—à–µ–º —Ñ–æ—Ä–º–∞—Ç–µ: –ø—Ä–µ–¥–µ–ª –ø—Ä–æ—á–Ω–æ—Å—Ç–∏ - 10-–µ —á–∏—Å–ª–æ (–∏–Ω–¥–µ–∫—Å 9), –ø—Ä–µ–¥–µ–ª —Ç–µ–∫—É—á–µ—Å—Ç–∏ - 11-–µ (–∏–Ω–¥–µ–∫—Å 10)
                            strength = numbers[9] if len(numbers) > 9 else 0
                            yield_strength = numbers[10] if len(numbers) > 10 else 0
                            
                            # –û—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ–µ —Å—É–∂–µ–Ω–∏–µ –∏ —É–¥–ª–∏–Ω–µ–Ω–∏–µ - –ø–æ—Å–ª–µ–¥–Ω–∏–µ –¥–≤–∞ —á–∏—Å–ª–∞
                            reduction = numbers[11] if len(numbers) > 11 else 0
                            elongation = numbers[12] if len(numbers) > 12 else 0
                            
                            data_rows.append({
                                '–ö–ª–µ–π–º–æ': sample_mark,
                                '–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞': temperature,
                                '–ü—Ä–µ–¥–µ–ª –ø—Ä–æ—á–Ω–æ—Å—Ç–∏': strength,
                                '–ü—Ä–µ–¥–µ–ª —Ç–µ–∫—É—á–µ—Å—Ç–∏': yield_strength,
                                '–û—Ç–Ω. —É–¥–ª.': elongation,
                                '–û—Ç–Ω. —Å—É–∂.': reduction
                            })
                            
                    except Exception as e:
                        continue
    
    # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–∞–±–ª–∏—Ü–∞—Ö, –ø—Ä–æ–±—É–µ–º –ø–∞—Ä—Å–∏—Ç—å —Ç–µ–∫—Å—Ç
    if not data_rows:
        return parse_protocol_from_text('\n'.join([p.text for p in doc.paragraphs]))
    
    return pd.DataFrame(data_rows)

def parse_protocol_from_text(text):
    """–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞–Ω–Ω—ã—Ö –∏–∑ —Ç–µ–∫—Å—Ç–∞ –ø—Ä–æ—Ç–æ–∫–æ–ª–∞"""
    lines = text.split('\n')
    data_rows = []
    
    for line in lines:
        # –ò—â–µ–º —Å—Ç—Ä–æ–∫–∏ —Å –∫–ª–µ–π–º–æ–º –æ–±—Ä–∞–∑—Ü–∞
        if re.search(r'\d+-\d+', line):
            # –£–±–∏—Ä–∞–µ–º –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã
            line_clean = re.sub(r'\s+', ' ', line.strip())
            
            # –ò—â–µ–º –≤—Å–µ —á–∏—Å–ª–∞ –≤ —Å—Ç—Ä–æ–∫–µ
            numbers = re.findall(r'[\d]+[.,]?\d*', line_clean)
            
            if len(numbers) >= 13:
                try:
                    # –ù–∞—Ö–æ–¥–∏–º –∫–ª–µ–π–º–æ
                    klem_match = re.search(r'(\d+-\d+)', line_clean)
                    sample_mark = klem_match.group(1) if klem_match else ""
                    
                    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —á–∏—Å–ª–∞
                    nums = [float(num.replace(',', '.').replace(' ', '')) for num in numbers]
                    
                    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –¥–∞–Ω–Ω—ã–µ (–∞–Ω–∞–ª–æ–≥–∏—á–Ω–æ —Ç–∞–±–ª–∏—á–Ω–æ–º—É –ø–∞—Ä—Å–µ—Ä—É)
                    temperature = int(nums[3]) if len(nums) > 3 else 20
                    strength = nums[9] if len(nums) > 9 else 0
                    yield_strength = nums[10] if len(nums) > 10 else 0
                    reduction = nums[11] if len(nums) > 11 else 0
                    elongation = nums[12] if len(nums) > 12 else 0
                    
                    data_rows.append({
                        '–ö–ª–µ–π–º–æ': sample_mark,
                        '–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞': temperature,
                        '–ü—Ä–µ–¥–µ–ª –ø—Ä–æ—á–Ω–æ—Å—Ç–∏': strength,
                        '–ü—Ä–µ–¥–µ–ª —Ç–µ–∫—É—á–µ—Å—Ç–∏': yield_strength,
                        '–û—Ç–Ω. —É–¥–ª.': elongation,
                        '–û—Ç–Ω. —Å—É–∂.': reduction
                    })
                    
                except Exception as e:
                    continue
    
    return pd.DataFrame(data_rows)

